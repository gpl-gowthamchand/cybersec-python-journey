num = 5 + 2.0 
print (num)
print (type(num))

# type casting to int 
num_int = int(5.7)
print (num_int)
print (type(num_int))

# type casting to float
num_float = float("3.14")
print (num_float)
print (type(num_float))

# type casting to string
message = str(42)
print (message)
print (type(message))

# type casting to boolean 
is_true = bool(1)
is_false = bool(0)
print (is_true)
print (type(is_true))
print (is_false)
print (type(is_false))


