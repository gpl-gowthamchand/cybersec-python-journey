# membership operators 

fruits = [ "grapes", "mango", "sapota"]


# in - returns true if value present in that sequence
is_Grapes_inList = "grapes" in fruits
print(is_Grapes_inList)

is_Apple_inList = "apple" in fruits
print(is_Apple_inList)

# not in - returns true if value not present in that sequence
is_Grapes_inList = "grapes" not in fruits
print(is_Grapes_inList)

is_Apple_inList = "apple" not in fruits
print(is_Apple_inList)
