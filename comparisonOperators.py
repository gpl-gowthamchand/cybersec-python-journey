## Comparison operators 

# == - equal to 
a = 2
b = 2
print(a == b)

# != - not equal to

a = 7
b = 6 
print( a != b )

# > - greater than

a = 7
b = 6 
print( a > b )

# < - less than

a = 7
b = 6 
print( a < b ) 

# >= - greater than or equal to

a = 7
b = 6 
print( a >= b )

# <= - less than or equal to

a = 7
b = 6 
print( a <= b )